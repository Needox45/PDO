    <p>Qu’est-ce que l’ambre gris, exactement ?

        L’ambre gris trouve son origine dans l’appareil digestif des grands cachalots. Les immenses cétacés consomment
        d’importantes quantités de calamars et autres seiches, mais n’en digèrent que la chair et pas certaines parties
        de leurs corps, comme les becs, par exemple.

        La plupart du temps, les grands cachalots expulsent ces éléments avant la digestion, précise le Muséum
        d’Histoire naturelle de Londres, au Royaume-Uni. Mais pas toujours. Parfois, ils finissent dans leurs intestins.
        Au fil des ans, ces éléments s’agglomèrent, jusqu’à former une masse compacte : c’est cela, le fameux ambre
        gris, que les grands cachalots expulsent ensuite dans les océans.

        Plusieurs théories s’opposent, sur la manière dont se fait cette expulsion. Certains chercheurs pensent que
        l’ambre gris est rejeté dans la mer en même temps que les déjections. D’autres sont d’avis que les cachalots
        vomissent cette substance. D’où son surnom de « vomi de baleine ».</p>





